"""PREPROCESSING THE DATA"""

import pandas as pd
car_data = pd.read_csv('/content/car data.csv')
car_data.drop(['Car_Name'], axis=1, inplace=True)
print(car_data.head())

car_data = pd.get_dummies(car_data, drop_first=True)

from sklearn.preprocessing import StandardScaler

scaler = StandardScaler()
numerical = ['Year', 'Present_Price', 'Kms_Driven']
car_data[numerical] = scaler.fit_transform(car_data[numerical])
car_data

"""SELECT  THE FEATURES IN X AND OUTPUT IN Y"""

X = car_data.drop('Selling_Price', axis=1)
Y = car_data['Selling_Price']

from sklearn.linear_model import LinearRegression
from sklearn.model_selection import train_test_split
X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.2, random_state=42)
model = LinearRegression()
model.fit(X_train, Y_train)
Y_pred = model.predict(X_test)
print(Y_pred[:20])